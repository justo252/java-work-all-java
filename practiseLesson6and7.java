public class practiseLesson6and7 {
    public static void main(String[] args) {
        //declaring and intializng variables for computations
         int num1 = 12;
         int num2 =13;
         int num3 = 23;

         //using compound operators where they intialize and perform computations at the same time.

        num1+=num2;
        num3-=num2;
        num3*=num2;

        System.out.println("Result One        =" + num1);
        System.out.println("Result Two        =" + num3 + "\n\n\n\n\n");

        //Lesson Seven

        num1++;
        num2++;
        num3--;

        System.out.println("Result Three      ="+ num1);
        System.out.println("Result Four       ="+ num2);
        System.out.println("Result Five       ="+ num3);

    }
}
