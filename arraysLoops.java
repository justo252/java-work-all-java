public class arraysLoops {
    public static void main(String[] args) {
        String[] cars = {
                "Range Rover",//index 0
                "Colt",//index 1
                "Note",//index 2
                "Atenza",//index 3
                "Paxful"//index 4

        };
        System.out.println("The following is a list of cars");
        //results
        for (int count=0;count<=4;count++){
            System.out.println(cars[count]);
        }
    }
}
